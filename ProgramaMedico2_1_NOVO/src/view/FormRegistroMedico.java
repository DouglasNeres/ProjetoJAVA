private void SalvarAtualizar(){
        if (btnSalvar.getText().equals("Salvar")) {
            String save = "\n" + "RM-" + txtNoRm.getText() + ";" + txtPaciente.getText() + ";" + txtEndereco.getText() + ";" + txtClasse.getText() + ";" + txtSala.getText();
            try (BufferedWriter bw = new BufferedWriter(new FileWriter(pathFile, true))) {
                bw.write(save);
                bw.flush();
                bw.newLine();
                bw.close();
                limpardados();
                mostrardados();
                dialog("Dados salvos com sucesso");
            } catch (IOException e) {
            }
        } else {
            try {
                AtualizacaoDoRegistroMedico();
                limpardados();
                mostrardados();
                btnDeletar.setEnabled(false);
                btnSalvar.setText("Salvar");
                dialog("Dados alterados com sucesso");
            } catch (IOException e) {
            }
        }
    }




private void Deletar() throws IOException {

        String ID, record;
        File tempDB = new File(pathFileTemp);
        File db = new File(pathFile);
        BufferedWriter bw;
        try (BufferedReader br = new BufferedReader(new FileReader(db))) {
            bw = new BufferedWriter(new FileWriter(tempDB));
            ID = "RM-"+txtNoRm.getText();
            while ((record = br.readLine()) != null) {
                if (record.contains(ID)) {
                    continue;
                }
                bw.write(record);
                bw.flush();
                bw.newLine();
            }
        }
        bw.close();
        db.delete();
        tempDB.renameTo(db);
    }
    
private void logicaClasse(){
       if (txtClasse.getText().isEmpty()&&txtEndereco.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Não foi digitada a especialidade médica");
       }
       else{ 
        switch (Integer.parseInt(txtClasse.getText())) {
            case 1:
                txtSala.setText("CARDIOLOGISTA");
                break;
            case 2:
                txtSala.setText("ORTOPEDISTA");
                break;
            default:
                txtSala.setText("CLINICO GERAL");
                break;
        }
       }
}






