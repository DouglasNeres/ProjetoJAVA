private void mostrardados() {
        try {
            listRegistroMedico.clear();
            String readLine;
            File file = new File(pathFile);
            if (!file.exists()) {
                file.createNewFile();
            }
                     
            BufferedReader bufReader;
            try (FileReader reader = new FileReader(file)) {
                bufReader = new BufferedReader(reader);
           
              while ((readLine = bufReader.readLine()) != null) {
                    if (readLine.equals("")) {
                        continue;
                    }
                    String[] splitData = readLine.split(";");
                    RegistroMedico rm = new RegistroMedico();
                    rm.setRm(splitData[0]);
                    rm.setPaciente(splitData[1]);
                    rm.setEndereco(splitData[2]);
                    rm.setClasse(Integer.parseInt(splitData[3]));
                    rm.setSala(splitData[4]);
                    listRegistroMedico.add(rm);
                }
             
                tablemodel.setList(listRegistroMedico);
                tbregmedico.setModel(tablemodel);
            }
            bufReader.close();
        } catch (IOException ex) {
            Logger.getLogger(FormRegistroMedico.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    
private void AtualizacaoDoRegistroMedico() throws IOException {
        String ID, record2;
        File db = new File(pathFile);
        File tempDB = new File(pathFileTemp);
        BufferedReader br2;
        try (BufferedWriter bw = new BufferedWriter(new FileWriter(tempDB))) {
            ID = "RM-"+txtNoRm.getText();
            br2 = new BufferedReader(new FileReader(db));
            while ((record2 = br2.readLine()) != null) {
                if (record2.contains(ID)) {
                    bw.write("RM-"+txtNoRm.getText()+";" + txtPaciente.getText() + ";" + txtEndereco.getText() + ";" + txtClasse.getText() + ";" + txtSala.getText());
                } else {
                    bw.write(record2);
                }
                bw.flush();
                bw.newLine();
            }
        }
        br2.close();
        db.delete();
        tempDB.renameTo(db);
    }

